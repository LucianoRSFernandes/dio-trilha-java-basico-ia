public class Incrementacao {
    public static void main(String[] args) {
        boolean variavel = true;
        //o sinal de exclamação inverte o valor da variável
        System.out.println(!variavel);
        System.out.println(variavel);
        // ao adicionarmos o incremento de negação a variável passa a ter o valor contrário definitivo
        variavel = !variavel;
        System.out.println(variavel);
    }
}
